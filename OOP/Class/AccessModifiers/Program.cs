/* ---------- AccessModifiers (Erişim Belirleyiciler) ----------
    1- Her yerden erişilebilir metotlar, field'lar, propery'ler ve classlar için bir tanımlamadır.
    2- Private : Yalnızca aynı sınıf içerisinden erişilebilir.
    3- Protected : Yalnızca aynı sınıf veya ondan türetilmiş alt class'lar tarafından erişilebilir.
    4- Sınıf ilk oluşturulduğunda genellikle internal varsayılan erişim belirleyici olarak gelir. 
Bu, sadece aynı proje(assembly, dll) içinde erişilebilir.
 
 
 
 
